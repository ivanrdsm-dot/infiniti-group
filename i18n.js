// Placeholder translations for form inputs
const placeholderMap = {
    contact_name: '#name',
    contact_email: '#email',
    contact_subject: '#subject',
    contact_message: '#message'
};

// All dev project "Acquire" buttons
const devBtnSelector = '.btn-outline.magnetic';

let currentLang = 'es';

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Update all mapped text elements
    for (const [key, selector] of Object.entries(translationMap)) {
        const el = document.querySelector(selector);
        if (el && translations[lang][key]) {
            // For reveal-text animated titles, handle specially
            if (el.dataset.animate === 'reveal-text' && el.classList.contains('animated')) {
                // Re-build word spans
                el.innerHTML = '';
                const text = translations[lang][key];
                text.split(' ').forEach((word, i) => {
                    const wordSpan = document.createElement('span');
                    wordSpan.classList.add('word');
                    const inner = document.createElement('span');
                    inner.classList.add('word-inner');
                    inner.textContent = word;
                    inner.style.transitionDelay = `${i * 0.06}s`;
                    inner.style.transform = 'translateY(0)';
                    wordSpan.appendChild(inner);
                    el.appendChild(wordSpan);
                });
            } else if (el.dataset.animate === 'reveal-text') {
                el.textContent = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    }

    // Update placeholders
    for (const [key, selector] of Object.entries(placeholderMap)) {
        const el = document.querySelector(selector);
        if (el && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    }

    // Update all dev "Acquire" buttons
    document.querySelectorAll(devBtnSelector).forEach(btn => {
        if (translations[lang].dev_btn) {
            btn.textContent = translations[lang].dev_btn;
        }
    });

    // Update toggle buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Save preference
    try { localStorage.setItem('infiniti-lang', lang); } catch (e) {}
}

// Initialize language toggle
document.addEventListener('DOMContentLoaded', () => {
    // Check stored preference
    try {
        const stored = localStorage.getItem('infiniti-lang');
        if (stored && translations[stored]) {
            currentLang = stored;
            if (stored !== 'es') setLanguage(stored);
        }
    } catch (e) {}

    // Bind toggle buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.dataset.lang;
            if (lang !== currentLang) {
                setLanguage(lang);
            }
        });
    });
});
